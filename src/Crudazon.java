
public class Crudazon {

	String codiceArticolo;
	String descrizioneArticolo;
	double prezzo;
	int pzDisponibili;
	
	public Crudazon(String codiceArticolo, String descrizioneArticolo, double prezzo, int pzDisponibili) {
		this.codiceArticolo = codiceArticolo;
		this.descrizioneArticolo = descrizioneArticolo;
		this.prezzo = prezzo;
		this.pzDisponibili = pzDisponibili;
	}
}
